let day =9;

switch(day){
    case 1:
        console.log("Monday - Chest Day");
        break;
    case 2:
        console.log("Tuesday - Back Day");
        break;
    case 3:
        console.log("Wednesday - Legs Day");
        break;
    case 4:
        console.log("Thursday - Arm Day");
        break;
    case 5:
        console.log("Friday - Shoulder Day");
        break;
    case 6:
        console.log("Saturday - Cardio");
        break;
    case 7:
        console.log("Sunday - Rest Day");
        break;
    default:
        console.log("Invalid day number");
}